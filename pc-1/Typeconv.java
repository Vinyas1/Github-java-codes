package jav1;

public class Typeconv {
	public static void main(String args[]) {
		int a=257;
		byte b;
		float g =323.567f;
		System.out.println("conversion of int to byte");
		b=(byte)a;
		System.out.println("conversion of float to int ");
		g=(int)g;
		System.out.println("conversion of int to float ");
		a=(float)a;
		
	}

}
